package com.jk.chattinglook;

public class Messageltem {

    public String name;
    public String message;
    public String time;
    public String profileUrl;
    public String room;


    public Messageltem() {


    }

    public Messageltem(String name, String message, String time, String profileUrl, String room) {
        this.name = name;
        this.message = message;
        this.time = time;
        this.profileUrl = profileUrl;
        this.room = room;
    }


}
